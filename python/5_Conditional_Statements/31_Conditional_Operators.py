def check_equal(x, y) -> bool:
    return x == y

def check_not_equal(x, y) -> bool:
    return x != y

def check_less_than(x, y) -> bool:
    return x < y

def check_greater_than(x, y) -> bool:
    return x > y

def check_less_than_or_equal(x, y) -> bool:
    return x <= y

def check_greater_than_or_equal(x, y) -> bool:
    return x >= y

